    private void updateMovie(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateMovie
        
        boolean success;
        
        String name = jTextFieldMovieName.getText();
        String genre = jComboBoxGenre.getSelectedItem().toString();
        String synopsis = jTextAreaSynopsis.getText();
        String releaseDate = jFormattedTextFieldReleaseDate.getText();
        
        try {
            MovieController movieController = new MovieController();
            success = movieController.updateMovie(id, name, genre, synopsis, releaseDate);
            
            if (success) {
                JOptionPane.showMessageDialog(null, "Successfully updated movie!");
            }else {
                JOptionPane.showMessageDialog(null, "Error updating movie");
            }

        } catch (Exception e) {
            System.out.println(e);
        }
        
    }//GEN-LAST:event_updateMovie

