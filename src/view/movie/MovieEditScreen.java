    public void getData(int id, String name, String genre, String synopsis, Date releaseDate) {
        this.id = id;
        jTextFieldMovieName.setText(name);
        jComboBoxGenre.setSelectedItem(genre);
        jTextAreaSynopsis.setText(synopsis);

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        jFormattedTextFieldReleaseDate.setText(sdf.format(releaseDate));
    }

